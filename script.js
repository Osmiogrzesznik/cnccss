var textifyObject= function(obj,replacer){
	outstr="";
	replacer = replacer? 
	     replacer  :  function(k,v){return k+":"+v+" "}

	Object.entries(obj).forEach(function(entry){
		key=entry[0];
		val=entry[1];
		
		outstr+=replacer(key,val)
		});

	return outstr;
}

